package Calculator;

public class Calculator {
    double add(int a, int b) {
        return a + b;
    } 

    double substrakt(int a, int b) {
        return a - b;
    }

    double multiply(int a, int b) {
        return a * b;
    }

    double divide(int a, int b) {
        return a / b;
    }
}
